package com.maxcriser.tweets_android.model;

public class ColorModel {

    public int R;
    public int G;
    public int B;

    public ColorModel(final int R, final int G, final int B) {
        this.R = R;
        this.G = G;
        this.B = B;
    }

    public int getR() {
        return R;
    }

    public int getG() {
        return G;
    }

    public int getB() {
        return B;
    }
}
